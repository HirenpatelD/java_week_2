package javaweek2;
/*18. Write a Java program to print the sum (addition), multiply, subtract, divide and
remainder of two numbers.
Test Data:
Input first number: 125
Input second number: 24
Expected Output :
125 + 24 = 149
125 - 24 = 101
125 x 24 = 3000
125 / 24 = 5
125 mod 24 = 5*/
public class Programme18_PrintTheNumber {
    public static void main(String [] args){
        System.out.println("addition of two number: " + (125 + 24));
        System.out.println("multiplication of two number: " + (125 * 24));
        System.out.println("subtraction of two number: " + (125 - 24));
        System.out.println("division of two number: " + (125 / 24));
        System.out.println("remainder of two number: " + (125 % 24));



    }


}
